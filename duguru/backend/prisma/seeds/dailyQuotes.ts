export async function seedDailyQuotes(): Promise<void> {
  console.log("üåü Seeding daily quotes (DailyContent)‚Ä¶");

  const baseDate = new Date("2026-01-01T12:00:00Z");

  let created = 0;
  let skipped = 0;

  for (let i = 0; i < 365; i++) {
    const date = new Date(baseDate);
    date.setUTCDate(date.getUTCDate() + i);
    const dateOnly = new Date(Date.UTC(date.getUTCFullYear(), date.getUTCMonth(), date.getUTCDate()));

    const quote = QUOTES[i % QUOTES.length]!;

    try {
      await prisma.dailyContent.upsert({
        where:  { date: dateOnly },
        update: { inspirationalQuote: quote },
        create: {
          date:               dateOnly,
          planet:             "sun",
          planetInfluence:    "Calculado dinamicamente.",
          moonPhase:          "new_moon",
          moonDescription:    "Calculado dinamicamente.",
          moonNextCycle:      dateOnly,
          inspirationalQuote: quote,
          alerts:             [],
        },
      });
      created++;
    } catch {
      skipped++;
    }
  }

  console.log(`‚úÖ Daily quotes: ${created} upserted, ${skipped} errors.`);
}
